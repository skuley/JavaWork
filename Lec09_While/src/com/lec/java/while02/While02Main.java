package com.lec.java.while02;

public class While02Main {

	public static void main(String[] args) {
		System.out.println("while 연습");
		
//		구구단 출력
		
		int num = 1;
		while(num <= 9) {
			System.out.println("2 * " + num + " = " + (2 * num));
			num++;
		}

	} // end main()

} // end class While02Main









